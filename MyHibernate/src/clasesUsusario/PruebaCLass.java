package clasesUsusario;

public class PruebaCLass{

	/**
	 * 
	 */

	private int num;
	private String s;
	
	public PruebaCLass(int n, String s) {
		this.num = n;
		this.s = s;
	}
	
	public PruebaCLass(String s) {
		this.s = s;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

	
}
